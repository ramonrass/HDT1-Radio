    private void bEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEstadoActionPerformed
        boolean estado = radio.isOn();

        if(estado == false){
            bFrequency.setEnabled(true);
            bBackward.setEnabled(true);
            bForward.setEnabled(true);
            bSave.setEnabled(true);
            lbFrequency.setEnabled(true);
            lbCH.setEnabled(true);
            pBotones.setEnabled(true);
            
            bCH1.setEnabled(true);
            bCH2.setEnabled(true);
            bCH3.setEnabled(true);
            bCH4.setEnabled(true);
            bCH5.setEnabled(true);
            bCH6.setEnabled(true);
            bCH7.setEnabled(true);
            bCH8.setEnabled(true);
            bCH9.setEnabled(true);
            bCH10.setEnabled(true);
            bCH11.setEnabled(true);
            bCH12.setEnabled(true);
            
            radio.OnOff(true);
            bEstado.setText("ON");
            lbStation.setText(radio.getStation());
        }
        else{
            bFrequency.setEnabled(false);
            bBackward.setEnabled(false);
            bForward.setEnabled(false);
            bSave.setEnabled(false);
            lbFrequency.setEnabled(false);
            lbCH.setEnabled(false);
            pBotones.setEnabled(false);
            
            bCH1.setEnabled(false);
            bCH2.setEnabled(false);
            bCH3.setEnabled(false);
            bCH4.setEnabled(false);
            bCH5.setEnabled(false);
            bCH6.setEnabled(false);
            bCH7.setEnabled(false);
            bCH8.setEnabled(false);
            bCH9.setEnabled(false);
            bCH10.setEnabled(false);
            bCH11.setEnabled(false);
            bCH12.setEnabled(false);
            
            radio.OnOff(false);
            bEstado.setText("OFF");
            lbStation.setText("");
        }
    }//GEN-LAST:event_bEstadoActionPerformed

    private void bFrequencyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bFrequencyActionPerformed
        String estado = radio.getFrequency();
        if(estado.equals("AM")){
            radio.setFrequency("FM");
            lbFrequency.setText(radio.getFrequency());
            
            radio.setStation("87.9");
            lbStation.setText(radio.getStation());
        }
        else{
            radio.setFrequency("AM");
            lbFrequency.setText(radio.getFrequency());
            
            radio.setStation("530");
            lbStation.setText(radio.getStation());
        }
    }//GEN-LAST:event_bFrequencyActionPerformed

