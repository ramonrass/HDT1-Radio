    private void bCH1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCH1ActionPerformed
        if(btnGuardar == true){
            radio.setMemory(0, Double.parseDouble(radio.getStation()));
        }
        else{
            String emisora = radio.selectMemory(0);
            if((Double.parseDouble(emisora)) != 0){
                radio.setStation(emisora);
                lbStation.setText(radio.getStation());
                lbCH.setText("CH 1");}
        }
        btnGuardar = false;
    }//GEN-LAST:event_bCH1ActionPerformed

