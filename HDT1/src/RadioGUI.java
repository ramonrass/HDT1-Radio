    private void addButtons(){
        javax.swing.JButton[] Botones = new javax.swing.JButton[12];
        for(int i=0;i<12;i++){
            for(int j=1;j<=2;j++)
            {
                javax.swing.JButton boton= new javax.swing.JButton(String.valueOf(i));
                Botones[i] = boton;
                pBotones.add(boton);
            }
        }
    }
    
    private void bEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEstadoActionPerformed
        boolean estado = radio.isOn();

        if(estado == false){
            bBackward.setEnabled(true);
            bForward.setEnabled(true);
            bSave.setEnabled(true);
            bFrequency.setEnabled(true);
            pBotones.setEnabled(true);
            radio.On(true);
            bEstado.setText("ON");
        }
        else{
            bBackward.setEnabled(false);
            bForward.setEnabled(false);
            bSave.setEnabled(false);
            bFrequency.setEnabled(false);
            pBotones.setEnabled(false);
            radio.On(false);
            bEstado.setText("OFF");
        }
    }//GEN-LAST:event_bEstadoActionPerformed

    private void bFrequencyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bFrequencyActionPerformed
        boolean estado = radio.getFrequency();
        if(estado == false){
            radio.setFrequency(true);
            lbFrequency.setText("FM");
        }
        else{
            radio.setFrequency(false);
            lbFrequency.setText("AM");
        }
    }//GEN-LAST:event_bFrequencyActionPerformed

